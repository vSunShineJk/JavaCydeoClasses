package day19_loops;

public class LoopBackwards {
    public static void main(String[] args) {

        for (int i = 50; i >= 0; i--){
            System.out.print(i + " ");
        }

        for (char letter = 'A'; letter <= 'Z'; letter++){
            System.out.print(letter + " ");
        }
    }
}
